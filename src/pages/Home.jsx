// Импортируем React и хук для работы с темой
import React from 'react';
import { useTheme } from '../context/ThemeContext';

// Компонент Home - главная страница приложения
const Home = () => {
  // Получаем текущую тему через хук useTheme()
  // isDark будет true для темной темы и false для светлой
  const { isDark } = useTheme();

  // Возвращаем разметку главной страницы
  return (
    // Основной контейнер страницы:
    // - container: ограничивает ширину контента
    // - mt-4: отступ сверху
    // - text-white: белый текст (если темная тема)
    <div className={`container mt-4 ${isDark ? 'text-white' : ''}`}>
      
      {/* Блок с приветствием */}
      {/* Динамические классы: */}
      {/* - p-4: внутренние отступы */}
      {/* - rounded: скругленные углы */}
      {/* - bg-secondary: серый фон (темная тема) */}
      {/* - bg-light: светлый фон (светлая тема) */}
      <div className={`p-4 rounded ${isDark ? 'bg-secondary' : 'bg-light'}`}>
        
        {/* Заголовок приветствия */}
        <h2>Добро пожаловать!</h2>
        
        {/* Текст-инструкция для пользователя */}
        <p>Выберите лабораторную работу из меню слева</p>
      </div>
    </div>
  );
};

// Экспортируем компонент для использования в роутинге
export default Home;