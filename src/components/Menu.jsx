import React from "react"; // Основная библиотека React
import { ListGroup } from "react-bootstrap"; // Компонент ListGroup из Bootstrap для создания списка

// Компонент Menu - отображает меню лабораторных работ
// На вход получает массив всех лабораторных работ, ID текущей выбранной работы и функцию для изменения выбранной работы
const Menu = ({ labs, activeLab, setActiveLab }) => {
  return (
    // Общий контейнер с отступом снизу (mb-4)
    <div className="mb-4">
      {/* Заголовок меню - стиль h4 (меньше h1-h3) с отступом снизу (mb-3) */}
      <h2 className="h4 mb-3">Лабораторные работы</h2>
      
      {/* Компонент ListGroup - стилизованный список из Bootstrap */}
      <ListGroup>
        {/* Перебираем массив лабораторных работ */}
        {labs.map((lab) => (
          // Элемент списка для каждой лабораторной работы
          <ListGroup.Item
            key={lab.id} // Уникальный ключ (обязательно для React при работе с массивами)
            action // Делаем элемент кликабельным (стиль кнопки)
            active={activeLab === lab.id} // Подсвечиваем активную работу
            onClick={() => setActiveLab(lab.id)} // Обработчик клика - меняем активную работу
          >
            {/* Отображаем название лабораторной работы */}
            {lab.title}
          </ListGroup.Item>
        ))}
      </ListGroup>
    </div>
  );
};

// Экспортируем компонент для использования в других частях приложения
export default Menu;